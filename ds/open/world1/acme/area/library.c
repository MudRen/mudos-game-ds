void init()
{
        add_action("do_sneak","sneak");
}
int do_sneak(string arg)
{
        object me;
        me=this_player();
        if(arg != "樓梯") return notify_fail("你要偷偷摸摸走向哪？\n");
        else {
            tell_object(me,"你偷偷摸摸的向樓梯走去。\n");
            me->move(__DIR__"library2f.c");
        }
        return 1;
}
